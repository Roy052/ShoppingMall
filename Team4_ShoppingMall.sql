CREATE TABLE IF NOT EXISTS ITEM(
	Item_Name VARCHAR(50),
	Item_Num INT,
	Manufacture_Date DATE,
	Store_Method VARCHAR(255),
	Importer VARCHAR(25),
	Customer_Conseling_Number VARCHAR(25),
	Price INT,
	Stock INT,
	Makers_No INT,
	PRIMARY KEY(Item_Num)
);

CREATE TABLE IF NOT EXISTS MAKERS(
	Maker_Num INT,
	Maker VARCHAR(25),
	Origin VARCHAR(25),
	Exchange_Return_Address VARCHAR(50),
	PRIMARY KEY(Maker_Num)
);

CREATE TABLE IF NOT EXISTS CUSTOMER(
	Name VARCHAR(15),
	ID VARCHAR(15),
	PW VARCHAR(15),
	Address VARCHAR(50),
	Tel VARCHAR(25),
	Sex CHAR(1),
	Age INT,
	Job VARCHAR(15),
	PRIMARY KEY(ID)
);
	
CREATE TABLE IF NOT EXISTS CATEGORY(
	Category_Num INT,
	Category_Name VARCHAR(25),
	Upper_Category_No INT,
	PRIMARY KEY(Category_Num)
);		

CREATE TABLE IF NOT EXISTS SHOP(
	Shop_Num INT,
	Address VARCHAR(15),
	PRIMARY KEY(Shop_Num)
);

CREATE TABLE IF NOT EXISTS CART(
	Customer_ID VARCHAR(15),
	Cart_Num INT,
	Delivery_Info VARCHAR(25),
	Ammount VARCHAR(15),
	PRIMARY KEY(Customer_ID,Cart_Num)
);

CREATE TABLE IF NOT EXISTS DISPLAY(
	Shop_Num INT,
	Item_Num INT,
	Ammount INT,
	PRIMARY KEY(Shop_Num, Item_Num)
);

CREATE TABLE IF NOT EXISTS ADD_CART(
	Customer_ID VARCHAR(15),
	Cart_Num INT,
	Item_Num INT,
	Ammount INT,
	PRIMARY KEY(Customer_ID, Cart_Num, Item_Num)
);

CREATE TABLE IF NOT EXISTS BUYING(
	Buying_Num INT,
	Customer_ID VARCHAR(15),
	Item_Num INT,
	Shop_Num INT,
	Buying_Date DATE,
	PRIMARY KEY(Buying_Num)
);

CREATE TABLE IF NOT EXISTS BELONG(
	Category_Num INT,
	Item_Num INT,
	PRIMARY KEY(Category_Num, Item_Num)
);

